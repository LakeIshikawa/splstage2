//! チート
/****************************************************************//**
*	チートの基本インターフェース。\n
*	このクラスから継承することでチート行動を作ります。
*	\see CheatsController
*	\nosubgrouping
********************************************************************/
class Cheat
{
	/************************************************************//**
	*	\name ﾗｲﾌ循環
	****************************************************************/
	/*@{*/
	protected:

		/************************************************************//**
		*	\param rKey 関連のキー (このキーが押されたらチートが発生する)
		****************************************************************/
		Cheat(int rKey) { mKey = rKey; }

		virtual ~Cheat(void) {};
	/*@}*/

	/************************************************************//**
	*	\name ｵｰﾊﾞｰﾗｲﾄﾞするﾒｿｯﾄﾞ
	****************************************************************/
	/*@{*/
	public:

	//! チートの実行
	/************************************************************//**
	*	チートしよう♪
	****************************************************************/	
	virtual void DoCheat() = 0;

	/*@}*/

	/************************************************************//**
	*	\name 使うﾒｿｯﾄﾞ
	*	処理に便利な機能を提供するﾒｿｯﾄﾞ。
	****************************************************************/	
	/*@{*/

	//! チートに関連しているキーを取得します
	int GetKey() { return mKey; }

	/*@}*/

protected:
	int mKey;
};